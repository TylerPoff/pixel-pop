package edu.northeastern.numad23sp_team26.pixel_pop.models;

public class Coordinate {

    private float x;
    private float y;

    public Coordinate(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
